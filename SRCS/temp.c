#include "so_long.h"

void print_map(t_map *m)
{
	int i;
	int j;

	i = 0;
	while (m->map[i])
	{
		j = 0;
		while (m->map[i][j])
		{
			printf("%c", m->map[i][j]);
			j++;
		}
		printf("\n");
		i++;
	}
}
